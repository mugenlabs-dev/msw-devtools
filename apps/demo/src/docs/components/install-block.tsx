const getInstallCommand = (pm: PackageManager, packages: string): string => {
  switch (pm) {
    case "npm": {
      return `npm install ${packages}`;
    }
    case "yarn": {
      return `yarn add ${packages}`;
    }
    case "pnpm": {
      return `pnpm add ${packages}`;
    }
    case "bun": {
      return `bun add ${packages}`;
    }
    default: {
      return `npm install ${packages}`;
    }
  }
};

